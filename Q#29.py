# Define a function that can receive two integral numbers in string form and compute their sum and then print
# it in console.





def strings(string_no1, string_no2):
    integer_num1 = int(string_no1)
    integer_num2 = int(string_no2)
    output = integer_num1 + integer_num2
    print(output)

num1 = "10"
num2 = "20"
strings(num1,num2)
