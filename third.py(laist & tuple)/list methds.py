#append method (add one element in the end)
list=[3,2,4,6]
list.append(7)
print(list)




#sorting method

#sorting in ascending order
list=[3,2,4,6]
list.sort()
print(list)




#sorting in descending order
list=[3,2,4,6]
list.sort(reverse=True)
print(list)



#reverse method
list=[3,2,4,6]
list.reverse()
print(list)



#insert method  (insert element at specific index)
list=[3,2,4,6]
list.insert(2,8)
print(list)



#remove method (to remove the first occurance of element)
list=[2,3,1,4,5,1,2,6]
list.remove(3)
print(list)



#pop method (remove element at particular index)
list=[2,3,1,4,5,1,2,6]
list.pop(4)
print(list)